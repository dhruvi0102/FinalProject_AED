    private void populateRqs() {
       
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        model.setRowCount(0);
        for (Shelter shelter : system.getShelterDirectory().getShelterList()) {
            System.out.println("cuttt " + shelter.getUserName() + "sjsjsj " + account.getUserName());
            if (shelter.getUserName().equals(account.getUserName())) {
                for (Request req : shelter.getRequests()) {
                    Object[] row = new Object[12];

                    row[0] = req.getPersonName();
                    row[1] = req.getPersonLocation();
                    row[2] = req.getArea();
                    row[3] = req.getTypeOfEmergency();
                    row[4] = req.getComments();
                    row[5] = req.getDate();
                    row[6] = req.getSelectedHospital();
                    row[7] = req.getSelectedFireDept();
                    row[8] = req.getSelectedPoliceDept();
                    row[9] = req.getSelectedShelter();
                    row[10] = req.getStatus();
                    row[11] = req.getRequestId();

                    model.addRow(row);
                }

            }
        }
    }
