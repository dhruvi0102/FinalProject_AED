    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        int selectRow = tblRequests.getSelectedRow();
        String patientName = "";
        String area = "";
        String status = "";
        String reqID = "";

        if (selectRow >= 0) {
            patientName = (String) tblRequests.getValueAt(selectRow, 0);
            area = (String) tblRequests.getValueAt(selectRow, 2);
            status = (String) tblRequests.getValueAt(selectRow, 10);
            reqID = (String) tblRequests.getValueAt(selectRow, 11);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        if (status.equalsIgnoreCase("Completed")) {
            JOptionPane.showMessageDialog(null, "This request has already been processed. Please Select Another Request.");
            return;
        } else {
            UnitManagementJPanel unitMngmt = new UnitManagementJPanel(userProcessContainer, account, system, patientName, area, status, reqID);
            userProcessContainer.add("unitMngmt", unitMngmt);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnNextActionPerformed


